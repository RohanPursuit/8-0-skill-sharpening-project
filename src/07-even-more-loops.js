/**
 * oddArray()
 * ---------------------
 * Returns a new array of only the odd numbers from the original array.
 * @param {number[]} array
 * @returns {array}
 *
 * EXAMPLE:
 *  oddArray([ 11, 15, 20, 22, 37 ]);
 *  //> [ 11, 15, 37 ]
 */
function oddArray(array) {
  let newArray = []
  for(const number of array){
    if(number % 2 === 1){
      newArray.push(number)
    }
  }
  return newArray
}

/**
 * evenArray()
 * ---------------------
 * Returns a new array of only the even numbers from the original array.
 * @param {number[]} array
 * @returns {array}
 *
 * EXAMPLE:
 *  evenArray([ 11, 15, 20, 22, 37 ]);
 *  //> [ 20, 22 ]
 */
function evenArray(array) {
  let newArray = []
  for(const number of array){
    if(number % 2 === 0){
      newArray.push(number)
    }
  }
  return newArray
}

/**
 * findSmallest()
 * ---------------------
 * Returns the smallest number from the array.
 * @param {number[]} array
 * @returns {number}
 *
 * EXAMPLE:
 *  findSmallest([ 0, 11, -2, 5 ]);
 *  //> -2
 */
function findSmallest(array) {
  let smallestNum = array[0]
  if(!array.length){return null}
  for( let i = 0; i < array.length; i+=2){
    if(array[i] > array[i+1] && array[i+1] < smallestNum){
      smallestNum = array[i+1]
    } else if (array[i] < smallestNum){
      smallestNum = array[i]
    }
  }
  return smallestNum
}
